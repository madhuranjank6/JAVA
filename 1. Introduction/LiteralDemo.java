import java.lang.*;

class LiteralDemo{
	public static void main(String arg[]){
		int a = 10; //int literal
		long b = 999999l; //long literal
		float c = 10.25f; //float literal
		double d = 5.6; //default value is also double
		double e = 5.6d; //double literal
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
	}
}