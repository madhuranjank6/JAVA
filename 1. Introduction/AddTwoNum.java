// Java Program to Add Two Numbers By taking user Input


import java.lang.*;
import java.util.*;

class AddTwoNum{
	public static void main(String args[]){
		Scanner scan = new Scanner(System.in);
		int a = scan.nextInt();
		int b = scan.nextInt();
		int c  = a + b;
		System.out.println("Sum = " + c);
	}
	
}